#include <stdio.h>

int main(){
    printf("Hello World\n"); // Quebrar Linha
    printf("Valor Inteiro: %d.\n", 10); // Mostra Valor Inteiro
    printf("Valor Flutuante: %.2f.\n", 3.134245); // Mostra Valor Flutuante
    printf("Dado de texto: %c.\n", 'a'); // 1 Letra
    printf("Dado de Texto: %s.\n", "testando"); // palavras com varias letras
}